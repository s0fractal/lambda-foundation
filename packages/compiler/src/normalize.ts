// ⚠️ MVP-заглушка: повертає "канонічний" рядок для простої стрілкової форми.
// Надалі сюди підключається повноцінний βη-нормалізатор і трансляція до 7 морфізмів.
export const normalize = (src: string): string => {
  return src
    .trim()
    .replace(/\s+/g, "")
    .replace(/\*2\b/g, "+x")   // x*2 → x+x (наївно, для демонстрації LOVE_ARC)
};